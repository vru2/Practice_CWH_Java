package JavaProg;

public class Increment_decrement_operator {
    public static void main(String[] args) {

        int a= 40;
        int b= a++;
        System.out.println(a);
        System.out.println(b);
        System.out.println(b);
        int c= ++ b;
        System.out.println(c);

        int y = 7;
        int x= ++y * 8;
        System.out.println(x);

        char D = 'E';
        D++;
        System.out.println(D);

        char A = 'a';
        A++;
        System.out.println(A);


    }

}
